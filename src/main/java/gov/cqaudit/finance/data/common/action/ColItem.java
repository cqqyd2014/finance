package gov.cqaudit.finance.data.common.action;

public class ColItem {
	String field_name;
	String col_type;
	String field_chinese_name;
	public String getField_name() {
		return field_name;
	}
	public void setField_name(String field_name) {
		this.field_name = field_name;
	}
	public String getCol_type() {
		return col_type;
	}
	public void setCol_type(String col_type) {
		this.col_type = col_type;
	}
	public String getField_chinese_name() {
		return field_chinese_name;
	}
	public void setField_chinese_name(String field_chinese_name) {
		this.field_chinese_name = field_chinese_name;
	}

}
